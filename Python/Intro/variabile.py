# variabila = zona din memoria calculatorului care tine date
# variabila = cutiuta cu valori

# declaram si initializam variabile:
marca_masina = 'Volvo'
model_masina = 'XC 60'
# nu putem pune spatii in numele variabilei
# o variabila incepe cu litera mica

# loosely typed - nu trebuie specificate tipurile de date

print(f'Am cumparat o masina marca: {marca_masina}')
print(f'Modelul cumparat este: {model_masina}')

# suprascrierea variabilelor:
model_masina = 'XC 60 facelift'

print(f'Dupa suprascriere, modelul devine: {model_masina}')

